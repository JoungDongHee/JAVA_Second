package chap_02;

public class _03_Operator3 {
    public static void main(String[] args) {
        // 비교 연산자
        System.out.println(5 > 3); // 5 는 3 보다 크다 (참 이면 true 거짓이면 false)
        System.out.println(5 >= 3); // 5 는 3보다 크거나 같다 (true)
        System.out.println(5 >= 5); // 5는 5보다 크거나 같다 (true)
        System.out.println(5 >= 7); // 5는 7 보다 크거나 같다 (false)
        System.out.println(5 > 5); // 5 는 5 보다 크다 (false)
        System.out.println("============================");
        System.out.println(5 < 3);
        System.out.println(5 <= 3);
        System.out.println(5 <= 5);
        System.out.println(5 <= 7);
        System.out.println(5 < 5);
        System.out.println("============================");
        System.out.println(5 == 3); // 5 는 3 보다 크다 (참 이면 true 거짓이면 false)
        System.out.println(5 == 5); // 5 는 3보다 크거나 같다 (false)
        System.out.println(5 != 5); // false
        System.out.println(5 != 3); //
    }
}
